public void refresh(){
DefaultTableModel newModel=new DefaultTableModel();
newModel.addColumn("Id");
newModel.addColumn("Nom");
newModel.addColumn("Prenom");
newModel.addColumn("Adresse");
newModel.addColumn("NumeroTel");
newModel.addColumn("NumeroFax");
newModel.addColumn("Email");
try
{
model.setRowCount(0);
s=con.createStatement();
ResultSet Rs= s.executeQuery("Select * from agentconsignataire");
while(Rs.next())
{
newModel.addRow(new Object[]{Rs.getInt("reference"),Rs.getString("nom"),Rs.getString("prenom"),Rs.getString("adresse"),Rs.getString("numeroTel"),Rs.getString("numeroFax"),Rs.getString("email")});
}
}
catch(Exception e)
{
JOptionPane.showMessageDialog(rootPane, e);
}
model = newModel;
agentsTable.setModel(model);
}
private void deplace(int i)
{
try
{
lastNameField.setText(model.getValueAt(i,1).toString());
firstNameField.setText(model.getValueAt(i,2).toString());
adresseField.setText(model.getValueAt(i,3).toString());
numeroTelField.setText(model.getValueAt(i,4).toString());
numeroFaxField.setText(model.getValueAt(i,5).toString());
emailField.setText(model.getValueAt(i,6).toString());
idField.setText(model.getValueAt(i,0).toString());
}
catch(Exception e)
{
System.err.println(e);
JOptionPane.showMessageDialog(null,"erreur de deplacement "+e.getLocalizedMessage());
}
}
