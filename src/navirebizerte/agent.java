    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked
        
String firstName = firstNameField.getText();
String lastName= lastNameField.getText();
String email = emailField.getText();
String adresse = adresseField.getText();
int numeroTel = Integer.parseInt(numeroTelField.getText());
int numeroFax = Integer.parseInt(numeroFaxField.getText());
try
{
PreparedStatement stmt1 = con.prepareStatement("insert into agentconsignataire(nom, prenom, adresse, numeroTel, numeroFax, email)VALUES(?, ?, ?, ?, ?, ?)");
stmt1.setString(1, lastName);
stmt1.setString(2, firstName);
stmt1.setString(3, adresse);
stmt1.setInt(4, numeroTel);
stmt1.setInt(5, numeroFax);
stmt1.setString(6, email);
stmt1.executeUpdate();
JOptionPane.showMessageDialog(null,"l'agent " + firstName + " " + lastName + " a été bien ajouté!");
numeroTelField.setText("");
lastNameField.setText("");
firstNameField.setText("");
adresseField.setText("");
numeroFaxField.setText("");
emailField.setText("");
refresh();
}
catch(Exception ex)
{
JOptionPane.showMessageDialog(null, "Une Erreur s'est arrivé lors de l'ajout du agent");
}
    }//GEN-LAST:event_addBtnMouseClicked

    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
try
{
int id = Integer.parseInt(idField.getText());
String firstName = firstNameField.getText();
String lastName= lastNameField.getText();
String email = emailField.getText();
String adresse = adresseField.getText();
int numeroTel = Integer.parseInt(numeroTelField.getText());
int numeroFax = Integer.parseInt(numeroFaxField.getText());
if(JOptionPane.showConfirmDialog(null,"Confirmer la modification","modification",JOptionPane.YES_NO_OPTION)==JOptionPane.OK_OPTION)
{
PreparedStatement stmt = con.prepareStatement("UPDATE agentconsignataire SET nom=?, prenom=?, adresse=?, numeroTel=?, numeroFax=?, email=? Where reference=?");
stmt.setString(1, lastName);
stmt.setString(2, firstName);
stmt.setString(3, adresse);
stmt.setInt(4, numeroTel);
stmt.setInt(5, numeroFax);
stmt.setString(6, email);
stmt.setInt(7, id);
stmt.executeUpdate();
}

}
catch(Exception e)
{
JOptionPane.showMessageDialog(null,"erreur de modification!!"+e.getMessage());
System.err.println(e);
}        
refresh();
    }//GEN-LAST:event_editBtnMouseClicked

